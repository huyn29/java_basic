public class Hangsx {
    private String name;
    private String quocGia;

    public Hangsx (String name, String quocGia){
        this.name = name;
        this.quocGia = quocGia;
    }

    public String getName() {
        return name;
    }
    public void setName(String name) {
        this.name = name;
    }
    public String getQuocGia() {
        return quocGia;
    }
    public void setQuocGia(String quocGia) {
        this.quocGia = quocGia;
    }
}
